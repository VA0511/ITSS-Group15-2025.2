CREATE TABLE "Member" (
  "id"                   INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "full_name"            varchar,
  "phone"                varchar,
  "email"                varchar,
  "gender"               varchar,
  "dob"                  date,
  "address"              text,
  "account_id"           int,
  "is_active"            boolean DEFAULT true,
  "roadmap_goal"         text,
  "member_free_schedule" text
