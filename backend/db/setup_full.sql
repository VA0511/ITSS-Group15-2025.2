CREATE TABLE "Subscription" (
  "id"                INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "member_id"         int,
  "package_id"        int,
  "registration_date" timestamp,
  "start_date"        date,
  "end_date"          date,
  "status"            varchar
);

CREATE TABLE "Facility" (
  "id"               INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "facility_name"    varchar,
  "facility_type"    varchar,
  "status"           varchar,
  "description"      text,
  "max_capacity"     int,
  "current_capacity" int DEFAULT 0,
  "amenities"        text,
