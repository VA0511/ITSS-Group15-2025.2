CREATE TABLE "Employee" (
  "id"         INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "full_name"  varchar,
  "phone"      varchar,
  "position"   varchar,
  "salary"     decimal,
  "account_id" int,
  "gender"     varchar,
  "dob"        date,
  "email"      varchar,
  "address"    text,
